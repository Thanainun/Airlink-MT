(function(d, $){

    var picker          = $('.theme-picker'),
        switcherColors  = $('.themecolor'),
        settingsBtn     = $('.settings-btn'),
        bodytag         = $('body'),
        colors          = [],
        deviceshots     = $('.deviceshot img'),
        pickericons     = $('.picker-icons');

    picker.isOpen = false;
    picker.origLeft = picker.offset().left;

    // Collect all the colors
    switcherColors.each(function(){
        colors.push( $(this).data('color') );
    });
    console.log(colors);
    // Colors Click Handler
    switcherColors.click(function(){
        var el      = $(this),
            color   = el.data('color');

        switcherColors.removeClass('selected');
        el.addClass('selected');

        // Clearout all the classes first
        bodytag[0].className = "";

        bodytag.addClass(color);
    });

    // Settings Button Toggle Handler
    settingsBtn.click(function(){

        // User CSS Transitions if they are avail
        if( Modernizr.csstransitions ){

            picker.toggleClass('on');

        }else{

            if (picker.isOpen) {
                picker.isOpen = false;
                picker.animate({
                    left: picker.origLeft
                }, 1000,'easeInOutElastic');
            }else{
                picker.isOpen = true;
                picker.animate({
                    left: '-40px'
                }, 1000,'easeInOutElastic');
            }

        }

    });

    // Handler for .ready() called.

        pickericons.on('click', function(){
            deviceshots.removeClass('active');
            pickericons.removeClass('selected');
            $(this).addClass('selected');
            console.log($(this).data('device'));
            $('.deviceshot .' + $(this).data('device') ).addClass('active');
        });

})(document, jQuery);